    private void deleteMarketBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteMarketBtnActionPerformed
        
        int selectedRow = manageMarketJTable.getSelectedRow();
        
        if(selectedRow >= 0)
        {
            Market market = (Market)manageMarketJTable.getValueAt(selectedRow, 0);
            business.getMarketDirectory().deleteMarket(market);
            populateTable();
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Please select a row.", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_deleteMarketBtnActionPerformed

    private void displayMarketBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayMarketBtnActionPerformed
        
        int selectedRow = manageMarketJTable.getSelectedRow(); 
        
        if(selectedRow >= 0)
        {
            Market market = (Market)manageMarketJTable.getValueAt(selectedRow, 0);
            DisplayMarketJPanel displayMarketJPanel = new DisplayMarketJPanel(userProcessContainer,business, market);
            userProcessContainer.add("DisplayMarketJPanel",displayMarketJPanel);
            CardLayout layout = (CardLayout)userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Please select a row.", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_displayMarketBtnActionPerformed

