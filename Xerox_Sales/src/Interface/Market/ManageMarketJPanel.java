    private void marketIdTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_marketIdTextFieldKeyTyped
        
        char ch = evt.getKeyChar();
        if(!(Character.isDigit(ch)) || (ch == KeyEvent.VK_BACK_SPACE) || (ch == KeyEvent.VK_DELETE))
        {
            evt.consume();
            if(Character.isAlphabetic(ch) || Character.isSpaceChar(ch))
            JOptionPane.showMessageDialog(null,"Enter numerical values only.");
        }
    }//GEN-LAST:event_marketIdTextFieldKeyTyped

    private void searchMarketBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchMarketBtnActionPerformed
        
        if(!marketIdTextField.getText().isEmpty())
        {
            Market market = business.getMarketDirectory().searchMarket(Integer.parseInt(marketIdTextField.getText()));
            
            if(market != null)
            {
                DisplayMarketJPanel displayMarketJPanel = new DisplayMarketJPanel(userProcessContainer, business, market);
                userProcessContainer.add("DisplayMarketJPanel",displayMarketJPanel);
                CardLayout layout = (CardLayout)userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Market does not exist.","Information",JOptionPane.INFORMATION_MESSAGE);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Please enter the MarketId.","Warning",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_searchMarketBtnActionPerformed

