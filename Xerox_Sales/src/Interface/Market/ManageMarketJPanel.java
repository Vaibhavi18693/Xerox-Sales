    public void populateTable()
    {
        DefaultTableModel defaultTableModel = (DefaultTableModel)manageMarketJTable.getModel();
        defaultTableModel.setRowCount(0);
        
        for(Market market : business.getMarketDirectory().getMarketDirectory())
        {
            Object row[] = new Object[3];
            row[0] = market;
            row[1] = market.getMarketType();
            row[2] = market.getTotalCustomers();
            defaultTableModel.addRow(row);
        }
    }

