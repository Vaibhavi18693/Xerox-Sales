    public void populateTable()
    {
        DefaultTableModel defaultTableModel = (DefaultTableModel)salesPersonJTable.getModel();
        defaultTableModel.setRowCount(0);
        
        for(SalesPerson salesPerson : business.getSalesPersonDirectory().getSalesPersonDirectory())
        {
            Object row[] = new Object[3];
            row[0] = salesPerson;
            row[1] = salesPerson.getPerson().getFullName();
            row[2] = salesPerson.getTotalNumberOfSales();
            defaultTableModel.addRow(row);
        }
    }
