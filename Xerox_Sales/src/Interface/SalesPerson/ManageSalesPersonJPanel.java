    private void salesPersonIdTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_salesPersonIdTextFieldKeyTyped

        char ch = evt.getKeyChar();
        if(!(Character.isDigit(ch)) || (ch == KeyEvent.VK_BACK_SPACE) || (ch == KeyEvent.VK_DELETE))
        {
            evt.consume();
            if(Character.isAlphabetic(ch) || Character.isSpaceChar(ch))
            JOptionPane.showMessageDialog(null,"Enter alphabetical values only.");
        }
    }//GEN-LAST:event_salesPersonIdTextFieldKeyTyped

    private void searchSalesPersonBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchSalesPersonBtnActionPerformed

        if(!salesPersonIdTextField.getText().isEmpty())
        {
            SalesPerson salesPerson = business.getSalesPersonDirectory().searchSalesPerson(Integer.parseInt(salesPersonIdTextField.getText()));

            if(salesPerson != null)
            {
                DisplaySalesPersonJPanel displaySalesPersonJPanel = new DisplaySalesPersonJPanel(userProcessContainer, salesPerson);
                userProcessContainer.add("DisplaySalesPersonJPanel",displaySalesPersonJPanel);
                CardLayout layout = (CardLayout)userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "The SalesPerson doesn't exist.","Information",JOptionPane.INFORMATION_MESSAGE);
            }
            salesPersonIdTextField.setText("");
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Please enter the CustomerId.","Warning",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_searchSalesPersonBtnActionPerformed

