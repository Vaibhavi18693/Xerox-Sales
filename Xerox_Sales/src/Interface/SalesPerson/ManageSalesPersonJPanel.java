    private void displaySalesPersonBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displaySalesPersonBtnActionPerformed

        int selectedRow = salesPersonJTable.getSelectedRow();

        if(selectedRow >= 0)
        {
            SalesPerson salesPerson = (SalesPerson)salesPersonJTable.getValueAt(selectedRow, 0);
            DisplaySalesPersonJPanel displaySalesPersonJPanel = new DisplaySalesPersonJPanel(userProcessContainer, salesPerson);
            userProcessContainer.add("DisplaySalesPersonJPanel",displaySalesPersonJPanel);
            CardLayout layout = (CardLayout)userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Please select a row.", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_displaySalesPersonBtnActionPerformed

    private void deleteSalesPersonBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteSalesPersonBtnActionPerformed

        int selectedRow = salesPersonJTable.getSelectedRow();

        if(selectedRow >= 0)
        {
            SalesPerson salesPerson = (SalesPerson)salesPersonJTable.getValueAt(selectedRow, 0);
            business.getSalesPersonDirectory().deleteSalesPerson(salesPerson);
            populateTable();
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Please select a row.", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_deleteSalesPersonBtnActionPerformed

    private void backBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backBtnActionPerformed

        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_backBtnActionPerformed


