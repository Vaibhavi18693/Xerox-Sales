    private void customerIdTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_customerIdTextFieldKeyTyped

        char ch = evt.getKeyChar();
        if(!(Character.isDigit(ch)) || (ch == KeyEvent.VK_BACK_SPACE) || (ch == KeyEvent.VK_DELETE))
        {
            evt.consume();
            if(Character.isAlphabetic(ch) || Character.isSpaceChar(ch))
            JOptionPane.showMessageDialog(null,"Enter alphabetical values only.");
        }
    }//GEN-LAST:event_customerIdTextFieldKeyTyped

    private void searchCuustomerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchCuustomerBtnActionPerformed

        if(!customerIdTextField.getText().isEmpty())
        {
            Customer customer = market.getCustomerDirectory().searchCustomer(Integer.parseInt(customerIdTextField.getText()));
            
            if(customer != null)
            {
                DisplayCustomerJPanel displayCustomerJPanel = new DisplayCustomerJPanel(userProcessContainer, customer);
                userProcessContainer.add("DisplayMarketJPanel",displayCustomerJPanel);
                CardLayout layout = (CardLayout)userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "The Customer doesn't exist.","Information",JOptionPane.INFORMATION_MESSAGE);
            }
            customerIdTextField.setText("");
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Please enter the CustomerId.","Warning",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_searchCuustomerBtnActionPerformed

