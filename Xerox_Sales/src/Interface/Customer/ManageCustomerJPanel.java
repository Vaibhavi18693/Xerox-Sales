    private void displayCustomerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayCustomerBtnActionPerformed

        int selectedRow = manageCustomerJTable.getSelectedRow();

        if(selectedRow >= 0)
        {
            Customer customer = (Customer)manageCustomerJTable.getValueAt(selectedRow, 0);
            DisplayCustomerJPanel displayCustomerJPanel = new DisplayCustomerJPanel(userProcessContainer, customer);
            userProcessContainer.add("DisplayCustomerJPanel",displayCustomerJPanel);
            CardLayout layout = (CardLayout)userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Please select a row.", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_displayCustomerBtnActionPerformed

    private void deleteCustomerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteCustomerBtnActionPerformed

        int selectedRow = manageCustomerJTable.getSelectedRow();

        if(selectedRow >= 0)
        {
            Customer customer = (Customer)manageCustomerJTable.getValueAt(selectedRow, 0);
            market.getCustomerDirectory().deleteCustomer(customer);
            populateTable();
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Please select a row.", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_deleteCustomerBtnActionPerformed

    private void backBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backBtnActionPerformed

        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_backBtnActionPerformed


