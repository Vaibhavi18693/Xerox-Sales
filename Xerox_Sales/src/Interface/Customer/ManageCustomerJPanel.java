    public void populateTable()
    {
        DefaultTableModel defaultTableModel = (DefaultTableModel)manageCustomerJTable.getModel();
        defaultTableModel.setRowCount(0);
        
        for(Customer customer : market.getCustomerDirectory().getCustomerDirectory())
        {
            Object row[] = new Object[5];
            row[0] = customer;
            row[1] = customer.getCustomerName();
            row[2] = customer.getPerson().getFullName();
            row[3] = customer.getPerson().getEmail();
            row[4] = customer.getPerson().getPhoneNum();
            defaultTableModel.addRow(row);
        }
    }
            
